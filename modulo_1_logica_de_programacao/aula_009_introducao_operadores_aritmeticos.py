# Aula 9: Operadores Aritméticos em Python

"""
Nesta aula, vamos explorar os operadores aritméticos fundamentais do Python.
Eles são essenciais para realizar cálculos matemáticos em nossos programas.
"""

# Vamos começar definindo dois números para usarmos nos exemplos:
a = 5
b = 3

# Soma (+)
resultado_soma = a + b
print(f"Soma: {a} + {b} = {resultado_soma}")

# Subtração (-)
resultado_subtracao = a - b
print(f"Subtração: {a} - {b} = {resultado_subtracao}")

# Multiplicação (*)
resultado_multiplicacao = a * b
print(f"Multiplicação: {a} * {b} = {resultado_multiplicacao}")

# Divisão (/)
resultado_divisao = a / b
print(f"Divisão: {a} / {b} = {resultado_divisao}")

# Divisão inteira (//)
resultado_divisao_inteira = a // b
print(f"Divisão inteira: {a} // {b} = {resultado_divisao_inteira}")

# Módulo (%)
resultado_modulo = a % b
print(f"Módulo: {a} % {b} = {resultado_modulo}")

# Exponenciação (**)
resultado_exponenciacao = a ** b
print(f"Exponenciação: {a} ** {b} = {resultado_exponenciacao}")

