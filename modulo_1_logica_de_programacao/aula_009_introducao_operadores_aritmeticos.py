# Aula 9: Introdução aos Operadores Aritméticos em Python

# Operadores aritméticos são usados para realizar operações matemáticas básicas.

# Soma (+)
a = 5
b = 3
soma = a + b
print("Soma:", soma)  # Saída: 8

# Subtração (-)
subtracao = a - b
print("Subtração:", subtracao)  # Saída: 2

# Multiplicação (*)
multiplicacao = a * b
print("Multiplicação:", multiplicacao)  # Saída: 15

# Divisão (/)
divisao = a / b
print("Divisão:", divisao)  # Saída: 1.666...

# Divisão inteira (//)
divisao_inteira = a // b
print("Divisão inteira:", divisao_inteira)  # Saída: 1

# Módulo (%)
modulo = a % b
print("Módulo:", modulo)  # Saída: 2

# Exponenciação (**)
exponenciacao = a ** b
print("Exponenciação:", exponenciacao)  # Saída: 125

